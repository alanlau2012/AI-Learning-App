import type {
  ArchitectureTradeoffDimension,
  ChecklistPhase,
  DecisionGuide,
} from '../types';

interface GuideInput {
  app: Array<[string, string, string[]]>;
  non: Array<[string, string, string[]]>;
  signals: Array<[string, string | undefined, string, string]>;
  tradeoffs: Array<[ArchitectureTradeoffDimension, string, string, string]>;
  questions: Array<[string, string, string[]]>;
  checks: Array<[ChecklistPhase, string, string]>;
  exec: {
    summary: string;
    businessValue: string;
    mainRisk: string;
    riskControl: string;
  };
}

function buildGuide(conceptId: string, input: GuideInput): DecisionGuide {
  return {
    applicableScenarios: input.app.map(([title, description, signals], index) => ({
      id: `${conceptId}-app-${index + 1}`,
      title,
      description,
      signals,
    })),
    nonApplicableScenarios: input.non.map(([title, description, signals], index) => ({
      id: `${conceptId}-non-${index + 1}`,
      title,
      description,
      signals,
    })),
    decisionSignals: input.signals.map(
      ([metricOrFact, threshold, interpretation, evidenceSource], index) => ({
        id: `${conceptId}-signal-${index + 1}`,
        metricOrFact,
        ...(threshold ? { threshold } : {}),
        interpretation,
        evidenceSource,
      }),
    ),
    tradeoffs: input.tradeoffs.map(([dimension, gain, cost, watchOut], index) => ({
      id: `${conceptId}-tradeoff-${index + 1}`,
      dimension,
      gain,
      cost,
      watchOut,
    })),
    reviewQuestions: input.questions.map(([question, whyAsk, goodAnswerSignals], index) => ({
      id: `${conceptId}-rq-${index + 1}`,
      question,
      whyAsk,
      goodAnswerSignals,
    })),
    implementationChecklist: input.checks.map(([phase, item, passSignal], index) => ({
      id: `${conceptId}-check-${index + 1}`,
      phase,
      item,
      passSignal,
    })),
    executiveExplanation: input.exec,
  };
}

